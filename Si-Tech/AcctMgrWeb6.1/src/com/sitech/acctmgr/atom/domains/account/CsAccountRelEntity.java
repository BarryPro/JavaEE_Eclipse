package com.sitech.acctmgr.atom.domains.account;

import com.alibaba.fastjson.annotation.JSONField;

public class CsAccountRelEntity {

	@JSONField(name = "CONTRACT_NO")
    private long 	contractNo;
	
	@JSONField(name = "REL_CONTRACT_NO")
    private long 	relContractNo;

	@JSONField(name = "ACCT_REL_TYPE")
    private String  acctRelType;

	@JSONField(name = "PAY_PRI")
    private long  	payPri;
	
	@JSONField(name = "PAY_VALUE")
    private long  	payValue;

	
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "CsAccountRelEntity [contractNo=" + contractNo
				+ ", relContractNo=" + relContractNo + ", acctRelType="
				+ acctRelType + ", payPri=" + payPri + ", payValue=" + payValue
				+ "]";
	}

	/**
	 * @return the payValue
	 */
	public long getPayValue() {
		return payValue;
	}

	/**
	 * @param payValue the payValue to set
	 */
	public void setPayValue(long payValue) {
		this.payValue = payValue;
	}

	/**
	 * @return the contractNo
	 */
	public long getContractNo() {
		return contractNo;
	}

	/**
	 * @param contractNo the contractNo to set
	 */
	public void setContractNo(long contractNo) {
		this.contractNo = contractNo;
	}

	/**
	 * @return the relContractNo
	 */
	public long getRelContractNo() {
		return relContractNo;
	}

	/**
	 * @param relContractNo the relContractNo to set
	 */
	public void setRelContractNo(long relContractNo) {
		this.relContractNo = relContractNo;
	}

	/**
	 * @return the acctRelType
	 */
	public String getAcctRelType() {
		return acctRelType;
	}

	/**
	 * @param acctRelType the acctRelType to set
	 */
	public void setAcctRelType(String acctRelType) {
		this.acctRelType = acctRelType;
	}

	/**
	 * @return the payPri
	 */
	public long getPayPri() {
		return payPri;
	}

	/**
	 * @param payPri the payPri to set
	 */
	public void setPayPri(long payPri) {
		this.payPri = payPri;
	}


}
