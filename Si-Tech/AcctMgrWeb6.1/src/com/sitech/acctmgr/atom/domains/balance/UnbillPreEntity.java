package com.sitech.acctmgr.atom.domains.balance;

/**
 * 未出帐余额查询接口记录实体类
 * Created by wangyla on 2017/3/1.
 */
public class UnbillPreEntity {
    private long conNo;
    private long balanceId;
    private String payType;
    private long initBalance;
    private long curBalance;
    private long wrtoffBalance;
    private String beginTime;
    private String endTime;

    public long getConNo() {
        return conNo;
    }

    public void setConNo(long conNo) {
        this.conNo = conNo;
    }

    public long getBalanceId() {
        return balanceId;
    }

    public void setBalanceId(long balanceId) {
        this.balanceId = balanceId;
    }

    public String getPayType() {
        return payType;
    }

    public void setPayType(String payType) {
        this.payType = payType;
    }

    public long getInitBalance() {
        return initBalance;
    }

    public void setInitBalance(long initBalance) {
        this.initBalance = initBalance;
    }

    public long getCurBalance() {
        return curBalance;
    }

    public void setCurBalance(long curBalance) {
        this.curBalance = curBalance;
    }

    public long getWrtoffBalance() {
        return wrtoffBalance;
    }

    public void setWrtoffBalance(long wrtoffBalance) {
        this.wrtoffBalance = wrtoffBalance;
    }

    public String getBeginTime() {
        return beginTime;
    }

    public void setBeginTime(String beginTime) {
        this.beginTime = beginTime;
    }

    public String getEndTime() {
        return endTime;
    }

    public void setEndTime(String endTime) {
        this.endTime = endTime;
    }

    @Override
    public String toString() {
        return "UnbillPreEntity{" +
                "conNo=" + conNo +
                ", balanceId=" + balanceId +
                ", payType='" + payType + '\'' +
                ", initBalance=" + initBalance +
                ", curBalance=" + curBalance +
                ", wrtoffBalance=" + wrtoffBalance +
                ", beginTime='" + beginTime + '\'' +
                ", endTime='" + endTime + '\'' +
                '}';
    }
}
