package com.sitech.acctmgr.atom.domains.bill;

import java.util.List;

/**
 * Created by wangyla on 2016/10/28.
 */
public class ConDetailDispEntity {
    private List<ContractDetail> detailList;
    /*客户预存总和*/
    private long custBalance;
    /*移动赠送预存总和*/
    private long mobBalance;
    /*用户预存总和*/
    private long totalBalance;

    public List<ContractDetail> getDetailList() {
        return detailList;
    }

    public void setDetailList(List<ContractDetail> detailList) {
        this.detailList = detailList;
    }

    public long getCustBalance() {
        return custBalance;
    }

    public void setCustBalance(long custBalance) {
        this.custBalance = custBalance;
    }

    public long getMobBalance() {
        return mobBalance;
    }

    public void setMobBalance(long mobBalance) {
        this.mobBalance = mobBalance;
    }

    public long getTotalBalance() {
        return totalBalance;
    }

    public void setTotalBalance(long totalBalance) {
        this.totalBalance = totalBalance;
    }

    @Override
    public String toString() {
        return "ConDetailDispEntity{" +
                "detailList=" + detailList +
                ", custBalance=" + custBalance +
                ", mobBalance=" + mobBalance +
                ", totalBalance=" + totalBalance +
                '}';
    }
}
