package com.sitech.acctmgr.atom.domains.cct;

import com.alibaba.fastjson.annotation.JSONField;

public class CctDynamicCreditInfoEntity {

	@JSONField(name = "ID_NO")
	private long idNo;

	@JSONField(name = "PHONE_NO")
	private String phoneNo;

	@JSONField(name = "LIMIT_OWE")
	private long limitOwe;

	@JSONField(name = "EFF_TIME")
	private String effTime;

	@JSONField(name = "EXP_TIME")
	private String expTime;

	@JSONField(name = "NOTES")
	private String notes;

	public long getIdNo() {
		return idNo;
	}

	public void setIdNo(long idNo) {
		this.idNo = idNo;
	}

	public String getPhoneNo() {
		return phoneNo;
	}

	public void setPhoneNo(String phoneNo) {
		this.phoneNo = phoneNo;
	}

	public long getLimitOwe() {
		return limitOwe;
	}

	public void setLimitOwe(long limitOwe) {
		this.limitOwe = limitOwe;
	}

	public String getEffTime() {
		return effTime;
	}

	public void setEffTime(String effTime) {
		this.effTime = effTime;
	}

	public String getExpTime() {
		return expTime;
	}

	public void setExpTime(String expTime) {
		this.expTime = expTime;
	}

	public String getNotes() {
		return notes;
	}

	public void setNotes(String notes) {
		this.notes = notes;
	}

	@Override
	public String toString() {
		return "CctDynamicCreditInfoEntity [idNo=" + idNo + ", phoneNo=" + phoneNo + ", limitOwe=" + limitOwe + ", effTime=" + effTime + ", expTime="
				+ expTime + ", notes=" + notes + "]";
	}

}
