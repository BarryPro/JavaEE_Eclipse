package com.sitech.acctmgr.atom.domains.query;

import com.alibaba.fastjson.annotation.JSONField;

public class ProvCityListEntity {
	
    @JSONField(name = "PROV_CODE")
    private String provCode;
    
    @JSONField(name = "PROV_NAME")
    private String provName;
    
    @JSONField(name = "LONG_CODE")
    private String longCode;
    
    @JSONField(name = "CITY_NAME")
    private String cityName;

	/**
	 * @return the provCode
	 */
	public String getProvCode() {
		return provCode;
	}

	/**
	 * @param provCode the provCode to set
	 */
	public void setProvCode(String provCode) {
		this.provCode = provCode;
	}

	/**
	 * @return the provName
	 */
	public String getProvName() {
		return provName;
	}

	/**
	 * @param provName the provName to set
	 */
	public void setProvName(String provName) {
		this.provName = provName;
	}

	/**
	 * @return the longCode
	 */
	public String getLongCode() {
		return longCode;
	}

	/**
	 * @param longCode the longCode to set
	 */
	public void setLongCode(String longCode) {
		this.longCode = longCode;
	}

	/**
	 * @return the cityName
	 */
	public String getCityName() {
		return cityName;
	}

	/**
	 * @param cityName the cityName to set
	 */
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
    
    
}
