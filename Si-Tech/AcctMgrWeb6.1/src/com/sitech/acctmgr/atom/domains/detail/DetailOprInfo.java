package com.sitech.acctmgr.atom.domains.detail;

import java.io.Serializable;
import java.util.Date;

/**
 * Created by wangyla on 2015/7/10.
 */
@SuppressWarnings("serial")
public class DetailOprInfo implements Serializable{

    private String phoneNO;
    private String detailType;
    private int beginDate;
    private int endDate;
    private int yearMonth;
    private int queryType; //0 查询 1 打印
    private String loginNO;
    private int oprCount;
    private String channelType;
    private int totalDate;
    private String queryFlag ; //0 按时间段 1 按账务年月
    private Date opTime;
    private String remark;


    public String getPhoneNO() {
        return phoneNO;
    }

    public void setPhoneNO(String phoneNO) {
        this.phoneNO = phoneNO;
    }

    public String getDetailType() {
        return detailType;
    }

    public void setDetailType(String detailType) {
        this.detailType = detailType;
    }

    public int getBeginDate() {
        return beginDate;
    }

    public void setBeginDate(int beginDate) {
        this.beginDate = beginDate;
    }

    public int getEndDate() {
        return endDate;
    }

    public void setEndDate(int endDate) {
        this.endDate = endDate;
    }

    public int getYearMonth() {
        return yearMonth;
    }

    public void setYearMonth(int yearMonth) {
        this.yearMonth = yearMonth;
    }

    public int getQueryType() {
        return queryType;
    }

    public void setQueryType(int queryType) {
        this.queryType = queryType;
    }

    public String getLoginNO() {
        return loginNO;
    }

    public void setLoginNO(String loginNO) {
        this.loginNO = loginNO;
    }

    public String getChannelType() {
        return channelType;
    }

    public void setChannelType(String channelType) {
        this.channelType = channelType;
    }

    public int getTotalDate() {
        return totalDate;
    }

    public void setTotalDate(int totalDate) {
        this.totalDate = totalDate;
    }

    public String getQueryFlag() {
        return queryFlag;
    }

    public void setQueryFlag(String queryFlag) {
        this.queryFlag = queryFlag;
    }

    public Date getOpTime() {
        return opTime;
    }

    public void setOpTime(Date opTime) {
        this.opTime = opTime;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }

    public int getOprCount() {
        return oprCount;
    }

    public void setOprCount(int oprCount) {
        this.oprCount = oprCount;
    }

    @Override
    public String toString() {
        return "DetailOprInfo{" +
                "phoneNO='" + phoneNO + '\'' +
                ", detailType='" + detailType + '\'' +
                ", beginDate=" + beginDate +
                ", endDate=" + endDate +
                ", yearMonth=" + yearMonth +
                ", queryType=" + queryType +
                ", loginNO='" + loginNO + '\'' +
                ", oprCount=" + oprCount +
                ", channelType='" + channelType + '\'' +
                ", totalDate=" + totalDate +
                ", queryFlag='" + queryFlag + '\'' +
                ", opTime=" + opTime +
                ", remark='" + remark + '\'' +
                '}';
    }
}
