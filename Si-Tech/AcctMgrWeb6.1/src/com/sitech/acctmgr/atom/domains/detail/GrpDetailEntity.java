package com.sitech.acctmgr.atom.domains.detail;

import java.util.List;

/**
 * Created by wangyla on 2016/12/21.
 */
public class GrpDetailEntity {
    private double totalShould;
    private double totalFavour;
    private long totalTime; //总时长
    private List<String> detailLines;
    private int lineCount;

    public double getTotalShould() {
        return totalShould;
    }

    public void setTotalShould(double totalShould) {
        this.totalShould = totalShould;
    }

    public double getTotalFavour() {
        return totalFavour;
    }

    public void setTotalFavour(double totalFavour) {
        this.totalFavour = totalFavour;
    }

    public long getTotalTime() {
        return totalTime;
    }

    public void setTotalTime(long totalTime) {
        this.totalTime = totalTime;
    }

    public List<String> getDetailLines() {
        return detailLines;
    }

    public void setDetailLines(List<String> detailLines) {
        this.detailLines = detailLines;
    }

    public int getLineCount() {
        return lineCount;
    }

    public void setLineCount(int lineCount) {
        this.lineCount = lineCount;
    }

    @Override
    public String toString() {
        return "GrpDetailEntity{" +
                "totalShould=" + totalShould +
                ", totalFavour=" + totalFavour +
                ", totalTime=" + totalTime +
                ", detailLines=" + detailLines +
                ", lineCount=" + lineCount +
                '}';
    }
}
