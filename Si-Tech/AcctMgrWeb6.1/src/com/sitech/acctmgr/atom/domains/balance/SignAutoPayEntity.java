package com.sitech.acctmgr.atom.domains.balance;

import com.alibaba.fastjson.annotation.JSONField;

public class SignAutoPayEntity {
	
	 /**
     */
	@JSONField(name="ID_NO")
    private long idNo;
	
	 /**
     */
	@JSONField(name="BUSI_TYPE")
    private String busiType;
	
	/**
     */
	@JSONField(name="PAY_MONEY")
    private long payMoney;
	
	 /**
     */
	@JSONField(name="BALANCE_LIMIT")
    private long balanceLimit;
	
	 /**
     */
	@JSONField(name="AUTO_FLAG")
    private String autoFlag;
	
	 /**
     */
	@JSONField(name="PAY_DAY")
    private String payDay;

	 /**
     */
	@JSONField(name="OP_TIME")
    private String opTime;	
	
	 /**
     */
	@JSONField(name="LOGIN_NO")
    private String loginNo;
	
	/**
	 * @return the payMoney
	 */
	public long getPayMoney() {
		return payMoney;
	}

	/**
	 * @param payMoney the payMoney to set
	 */
	public void setPayMoney(long payMoney) {
		this.payMoney = payMoney;
	}

	/**
	 * @return the balanceLimit
	 */
	public long getBalanceLimit() {
		return balanceLimit;
	}

	/**
	 * @param balanceLimit the balanceLimit to set
	 */
	public void setBalanceLimit(long balanceLimit) {
		this.balanceLimit = balanceLimit;
	}

	/**
	 * @return the autoFlag
	 */
	public String getAutoFlag() {
		return autoFlag;
	}

	/**
	 * @param autoFlag the autoFlag to set
	 */
	public void setAutoFlag(String autoFlag) {
		this.autoFlag = autoFlag;
	}

	/**
	 * @return the idNo
	 */
	public long getIdNo() {
		return idNo;
	}

	/**
	 * @param idNo the idNo to set
	 */
	public void setIdNo(long idNo) {
		this.idNo = idNo;
	}

	/**
	 * @return the busiType
	 */
	public String getBusiType() {
		return busiType;
	}

	/**
	 * @param busiType the busiType to set
	 */
	public void setBusiType(String busiType) {
		this.busiType = busiType;
	}
	/**
	 * @return the payDay
	 */
	public String getPayDay() {
		return payDay;
	}
	/**
	 * @param payDay the payDay to set
	 */
	public void setPayDay(String payDay) {
		this.payDay = payDay;
	}
	
	/**
	 * @return the opTime
	 */
	public String getOpTime() {
		return opTime;
	}
	
	/**
	 * @param opTime the opTime to set
	 */
	public void setOpTime(String opTime) {
		this.opTime = opTime;
	}
	
	/**
	 * @return the loginNo
	 */
	public String getLoginNo() {
		return loginNo;
	}
	
	/**
	 * @param loginNo the loginNo to set
	 */
	public void setLoginNo(String loginNo) {
		this.loginNo = loginNo;
	}
	

}
