package com.sitech.acctmgr.atom.domains.invoice.elecInvoice;

import com.thoughtworks.xstream.annotations.XStreamAlias;
import com.thoughtworks.xstream.annotations.XStreamAsAttribute;

/**
 * 发票物流信息
 * 
 * @author liuhl_bj
 *
 */
public class InvoiceFlowInfo {

	@XStreamAlias("class")
	@XStreamAsAttribute
	private String classalias;

	// 承运公司
	@XStreamAlias("CYGS")
	private String cygs;

	// 送货时间
	@XStreamAlias("SHSJ")
	private String shsj;

	// 物流单号
	@XStreamAlias("WLDH")
	private String wldh;

	// 送货地址
	@XStreamAlias("SHDZ")
	private String shdz;

	public String getCygs() {
		return cygs;
	}

	public void setCygs(String cygs) {
		this.cygs = cygs;
	}

	public String getShsj() {
		return shsj;
	}

	public void setShsj(String shsj) {
		this.shsj = shsj;
	}

	public String getWldh() {
		return wldh;
	}

	public void setWldh(String wldh) {
		this.wldh = wldh;
	}

	public String getShdz() {
		return shdz;
	}

	public void setShdz(String shdz) {
		this.shdz = shdz;
	}

}
