package com.sitech.acctmgr.atom.domains.bill;

/**
 * 功能：TD流量查询数据实体
 * Created by wangyla on 2016/8/9.
 */
public class TDDataEntity {
    private long idNo;
    private String phoneNo;
    private int totalDate;
    private int dataMonth;
    private long gprsData;

    public long getIdNo() {
        return idNo;
    }

    public void setIdNo(long idNo) {
        this.idNo = idNo;
    }

    public String getPhoneNo() {
        return phoneNo;
    }

    public void setPhoneNo(String phoneNo) {
        this.phoneNo = phoneNo;
    }

    public int getDataMonth() {
        return dataMonth;
    }

    public void setDataMonth(int dataMonth) {
        this.dataMonth = dataMonth;
    }

    public int getTotalDate() {
        return totalDate;
    }

    public void setTotalDate(int totalDate) {
        this.totalDate = totalDate;
    }

    public long getGprsData() {
        return gprsData;
    }

    public void setGprsData(long gprsData) {
        this.gprsData = gprsData;
    }

    @Override
    public String toString() {
        return "TDDataEntity{" +
                "idNo=" + idNo +
                ", phoneNo='" + phoneNo + '\'' +
                ", totalDate=" + totalDate +
                ", dataMonth=" + dataMonth +
                ", gprsData=" + gprsData +
                '}';
    }
}
