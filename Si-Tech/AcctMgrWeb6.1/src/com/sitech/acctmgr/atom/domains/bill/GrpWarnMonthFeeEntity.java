package com.sitech.acctmgr.atom.domains.bill;

/**
 * 功能：国际漫游预警工单查询数据实体
 * 
 */
public class GrpWarnMonthFeeEntity {
	
    private String totalDate;
    private String phoneNo;
    private long monthFee;
    private long monthArpu;

    public String getTotalDate() {
		return totalDate;
	}

	public void setTotalDate(String totalDate) {
		this.totalDate = totalDate;
	}

	public String getPhoneNo() {
		return phoneNo;
	}

	public void setPhoneNo(String phoneNo) {
		this.phoneNo = phoneNo;
	}

	public long getMonthFee() {
		return monthFee;
	}

	public void setMonthFee(long monthFee) {
		this.monthFee = monthFee;
	}

	public long getMonthArpu() {
		return monthArpu;
	}

	public void setMonthArpu(long monthArpu) {
		this.monthArpu = monthArpu;
	}

	@Override
    public String toString() {
        return "GrpWarnMonthFeeEntity{" +
                "totalDate=" + totalDate +
                ", phoneNo='" + phoneNo + '\'' +
                ", monthFee=" + monthFee +
                ", monthArpu=" + monthArpu +
                '}';
    }
}
